let colorCode = "r";
switch (colorCode) {
  case "r":
    console.log("RED 游댮游댮");
    break;
  case "g":
    console.log("GREEN 游릭游릭");
    break;
  case "b":
    console.log("BLUE 游댯游댯");
    break;
  case "y":
    console.log("YELLOW 游리游리");
    break;
  case "o":
    console.log("ORANGE 游멇");
    break;
  default:
    console.log("Invalid color code. Please enter r, g, b, y, or o.");
    break;
}
