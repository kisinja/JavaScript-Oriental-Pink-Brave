let colorCode = "r";
switch (colorCode) {
  case "r":
    console.log("RED 🔴🔴");
    break;
  case "g":
    console.log("GREEN 🟢🟢");
    break;
  case "b":
    console.log("BLUE 🔵🔵");
    break;
  case "y":
    console.log("YELLOW 🟡🟡");
    break;
  case "o":
    console.log("ORANGE 🟠🟠");
    break;
  default:
    console.log("Invalid color code. Please enter r, g, b, y, or o.");
    break;
}
